package com.lti.model;

public class Customer {

	private int custId;
	private int custName;
	public Customer() {
		
	}
	public int getCustId() {
		return custId;
	}
	public void setCustId(int custId) {
		this.custId = custId;
	}
	public int getCustName() {
		return custName;
	}
	public void setCustName(int custName) {
		this.custName = custName;
	}
	
}
