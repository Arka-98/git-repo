package com.lti.model;

public class Transaction {

	private int transacId;
	private int transacDate;
	private int transacAmount;
	public int getTransacId() {
		return transacId;
	}
	public void setTransacId(int transacId) {
		this.transacId = transacId;
	}
	public int getTransacDate() {
		return transacDate;
	}
	public void setTransacDate(int transacDate) {
		this.transacDate = transacDate;
	}
	public int getTransacAmount() {
		return transacAmount;
	}
	public void setTransacAmount(int transacAmount) {
		this.transacAmount = transacAmount;
	}
}
